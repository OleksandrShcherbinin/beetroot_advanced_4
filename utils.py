def add_two_numbers(num1: int | float, num2: int | float) -> int | float:
    return num1 + num2


def sub_two_numbers(num1: int | float, num2: int | float) -> int | float:
    return num1 - num2


def multiply_two_numbers(num1: int | float, num2: int | float) -> int | float:
    return num1 * num2
